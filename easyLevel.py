# 1. Add two numbers
def sum(a, b):
    return a + b


# 2. Square of a number
def square(n):
    return n * n


# 3. Check even or odd
def evenOdd(n):
    if n % 2 == 0:
        return "Even"
    else:
        return "Odd"


# 4. Print "Hello" 5 times
def print_hello():
    for i in range(5):
        print("Hello")


# 5. Maximum of two numbers
def max(a, b):
    if a > b:
        return a
    else:
        return b
