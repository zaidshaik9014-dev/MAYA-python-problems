from turtle import*
speed(-40)
bgcolor("white")
colors = ["orange", "black", "red", "purple", "brown"]
hideturtle()
for i in range (500):
    goto(0, 10)
    color(colors[i % 4])
    forward(130)
    left(3)
    circle(40)
    forward(130)
    right(180)
    circle(40)
    forward(130)
    right(180)
    circle(50)
    backward(130)
    left(180)
    backward(260)
    right(360)
    circle(80)
    forward(130)
    left(200)
done()